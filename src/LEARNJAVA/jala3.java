package LEARNJAVA;
//PROGRAM FOR EQUAL AND NOT EQUAL OPERATOR AND CHECK WHETHER TWO NUMBERS ARE EQUAL OR NOT
public class jala3 {
    public static void main(String[] args) {
        int a = 2;
        int b = 4;
        if (a == b) {
            System.out.println("BOTH ARE EQUAL");
        } else if (a != b) {
            System.out.println("BOTH ARE NOT EQUAL");
        }
    }
}
